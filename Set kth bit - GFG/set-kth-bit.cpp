class Solution
{
public:
    int setKthBit(int N, int K)
    {
        int bit = 1;
        while(K != 0){
            K--;
            bit = bit << 1;
        }
        
        return (N | bit);
    }
    
};




//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N, K;
        cin >> N >> K;
        
        Solution ob;
        int ans = ob.setKthBit(N,K);
        cout << ans << endl;
    }
    return 0;
}
