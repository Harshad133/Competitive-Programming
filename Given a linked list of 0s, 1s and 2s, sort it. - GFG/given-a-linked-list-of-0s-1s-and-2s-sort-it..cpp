class Solution
{
    public:
    
    void sort(vector<int>& nums) {
        int tmp = 0, low = 0, mid = 0, high = nums.size() - 1;
    
        while(mid <= high)
        {
            if(nums[mid] == 0)
            {
                tmp = nums[low];
                nums[low] = nums[mid];
                nums[mid] = tmp;
                low++;
                mid++;
            }
            else if(nums[mid] == 1)
            {
                mid++;
            }
            else if(nums[mid] == 2)
            {
                tmp = nums[high];
                nums[high] = nums[mid];
                nums[mid] = tmp;
                high--;
            }
        }
    }
    
    //Function to sort a linked list of 0s, 1s and 2s.
    Node* segregate(Node *head) {
        vector<int> temp;
        Node *i = head;
        while(i != NULL){
            temp.push_back(i->data);
            i = i->next;
        }
        
        sort(temp);
        
        i = head;
        int j = 0;
        while(i != NULL){
            i->data = temp[j++];
            i = i->next;
        }
        
        return head;
    }
};


